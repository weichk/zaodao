package com.acooly.zaodao.platform.enums;

import com.acooly.core.utils.enums.Messageable;

import java.util.ArrayList;
import java.util.LinkedHashMap;
import java.util.List;
import java.util.Map;

/**
 * 服务费收费标准
 *
 * @author xiaohong
 * @create 2018-05-28 18:22
 **/
public enum ServiceFeeScaleType implements Messageable {

    fixed_fee("fixed_fee", "固定收费"),
    percent_fee("percent_fee", "百分比收费")
    ;

    private final String code;
    private final String message;

    private ServiceFeeScaleType(String code, String message) {
        this.code = code;
        this.message = message;
    }

    public String getCode() {
        return code;
    }

    public String getMessage() {
        return message;
    }

    public String code() {
        return code;
    }

    public String message() {
        return message;
    }

    public static Map<String, String> mapping() {
        Map<String, String> map = new LinkedHashMap<String, String>();
        for (ServiceFeeScaleType type : values()) {
            map.put(type.getCode(), type.getMessage());
        }
        return map;
    }

    /**
     * 通过枚举值码查找枚举值。
     *
     * @param code
     *            查找枚举值的枚举值码。
     * @return 枚举值码对应的枚举值。
     * @throws IllegalArgumentException
     *             如果 code 没有对应的 Status 。
     */
    public static ServiceFeeScaleType find(String code) {
        for (ServiceFeeScaleType status : values()) {
            if (status.getCode().equals(code)) {
                return status;
            }
        }
        return null;
    }

    /**
     * 获取全部枚举值。
     *
     * @return 全部枚举值。
     */
    public static List<ServiceFeeScaleType> getAll() {
        List<ServiceFeeScaleType> list = new ArrayList<ServiceFeeScaleType>();
        for (ServiceFeeScaleType status : values()) {
            list.add(status);
        }
        return list;
    }

    /**
     * 获取全部枚举值码。
     *
     * @return 全部枚举值码。
     */
    public static List<String> getAllCode() {
        List<String> list = new ArrayList<String>();
        for (ServiceFeeScaleType status : values()) {
            list.add(status.code());
        }
        return list;
    }

}
